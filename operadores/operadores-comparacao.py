'''
 == Equal
 != Not Equal
 >  Greater than
 <  Less than
 >= Greater than or equal to
 <= Less than or equal to 
'''

operadores = 10 == 10
print(operadores)

operadores = 'banana' != 'banana'
print(operadores)

operadores = 10 < 9
print(operadores)

operadores = 10 > 9
print(operadores)

operadores = 10 <= 9
print(operadores)

operadores = 10 >= 10
print(operadores)
